"""
Infrastructure: NLP enrichment pipeline.

Two-stage classifier:
  1. Baseline – keyword/rule-based classifier (fast, no training needed).
  2. ML classifier – TF-IDF + LogisticRegression trained on weak labels
     generated by the baseline (auto-trains if no model file exists).

Sentiment: VADER (over TextBlob) because:
  - Specifically tuned for short news headlines / social text.
  - No external corpus download required at runtime.
  - Compound score maps cleanly to positive/neutral/negative.

Entity extraction: lightweight regex heuristic (capitalised phrases) to
avoid the ~40 MB spaCy model download requirement for MVP.
Replace with spaCy for production.
"""
